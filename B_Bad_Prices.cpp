void solve()
{
    int n;
    cin >> n;
    vector<int> v(n);
    for (int i = 0; i < n; ++i)
    {
        cin >> v[i];
    }
    int cnt = 0;
    int mn = 1e8;
    for (int i = n - 1; i >= 0; --i)
    {
        if (v[i] > mn)
            cnt++;
        mn = min(mn, v[i]);
    }
    cout << cnt << endl;
}
int main()
{
    fastIO();
    int tc;
    cin >> tc;
    while (tc--)
    {
        solve();
    }
    return 0;
}