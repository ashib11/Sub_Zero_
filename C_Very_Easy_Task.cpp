ll n, x, y;
bool f(ll mid)
{
    return (mid/x) + (mid/y) >= n;
}
void solve()
{

    cin >> n >> x >> y;
    ll a = min(x, y);
    ll l = 0, r = 1e12, ans = 0;
    n--;
    while (l <= r)
    {
        ll mid = (l + r) / 2;
        if (f(mid))
        {
            ans = mid;
            r = mid - 1;
        }
        else
        {
            l = mid + 1;
        }
    }
    cout << ans + a << endl;
}

int main()
{
    fastIO();
    int tc = 1;
    // cin >> tc;
    while (tc--)
    {
        solve();
    }
    return 0;
}
