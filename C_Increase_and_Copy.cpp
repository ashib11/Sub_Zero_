bool isPrime(int n)
{
    if (n <= 1)
        return false;
    if (n == 2)
        return true;
    if (n % 2 == 0)
        return false;
    for (int i = 3; i * i <= n; i += 2)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
void solve()
{
    int n;
    cin >> n;
    if (n == 1)
    {
        cout << 0 << endl;
        return;
    }
    int ans = n;
    for (int i = 1; (i * i) <= n; ++i)
    {
        int first = i - 1;
        int cpy = (n - 1) / i;
        int val = first + cpy;
        ans = min(ans, val);
    }
    cout << ans << endl;
}

