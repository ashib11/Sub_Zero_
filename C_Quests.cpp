void solve()
{
    int n, k;
    cin >> n >> k;

    vector<int> a(n), b(n);
    for (int i = 0; i < n; ++i)
    {
        cin >> a[i];
    }
    for (int i = 0; i < n; ++i)
    {
        cin >> b[i];
    }
    ll sum = 0;
    ll f = -1;
    int mx = 0;
    for (int i = 0; i < min(n, k); ++i)
    {
        sum += a[i];
        mx = max(mx, b[i]);
        ll ans = sum + mx * (k - i - 1);
        f = max(ans, f);
    }
    cout << f << endl;
}

int main()
{
    fastIO();
    int tc = 1;
    cin >> tc;
    while (tc--)
    {
        solve();
    }
    return 0;
}
